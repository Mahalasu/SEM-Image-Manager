class FileInfo:

    def __init__(self):
        self.image_path = ''
        self.craft_image_path = ''
        self.force_image_path = ''
        self.bark_image_path = ''

    def reset(self):
        self.image_path = ''
        self.craft_image_path = ''
        self.force_image_path = ''
        self.bark_image_path = ''
